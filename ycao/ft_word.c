#include "libft.h"

int		ft_word(char const *s, char c)
{
	int		i;
	int		num;
	int		mark;

	i = 0;
	num = 0;
	mark = 1;
	while (s[i])
	{
		if (s[i] == c)
			mark = 1;
		if (s[i] != c && mark == 1)
		{
			mark = 0;
			num++;
		}
		i++;
	}
	return (num);
}
